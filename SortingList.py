# Sorting list using sort() method.

# List of numbers.
numbers = [2, 3, 9, 1, 5, 4, 2.5, 7, 19, 13, 10]
numbers.sort()

print(numbers)

# List of strings.
items = ["chair", "board", "door", "desk", "table", "window"]
items.sort()

print(items)

# Using the reverse=True to sort the list in descending order.
numbers.sort(reverse=True)
print(numbers)